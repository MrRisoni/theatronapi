package hello.bookPojos;

public class Seat {

    private String type;
    private String seat;


    public Seat()
    {
    }


    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getSeat() {
        return seat;
    }

    public void setSeat(String seat) {
        this.seat = seat;
    }
}
